export const allIdioms = [...easyIdioms, ...hardIdioms];

export function getIdiomsByDifficulty(difficulty) {
  if (difficulty === 'easy') return easyIdioms;
  if (difficulty === 'hard') return hardIdioms;
  return allIdioms;
}

export function getIdiomByLastChar(char, usedIdioms, difficulty = 'all') {
  const idiomList = getIdiomsByDifficulty(difficulty);
  const available = idiomList.filter(idiom => 
    idiom.word[0] === char && !usedIdioms.includes(idiom.word)
  );
  if (available.length === 0) return null;
  return available[Math.floor(Math.random() * available.length)];
}

export function getRandomIdiom(difficulty = 'all') {
  const idiomList = getIdiomsByDifficulty(difficulty);
  return idiomList[Math.floor(Math.random() * idiomList.length)];
}

export function getIdiomMeaning(word, difficulty = 'all') {
  const idiomList = getIdiomsByDifficulty(difficulty);
  const idiom = idiomList.find(i => i.word === word);
  return idiom ? idiom.meaning : null;
}

export function isValidIdiom(word, difficulty = 'all') {
  const idiomList = getIdiomsByDifficulty(difficulty);
  return idiomList.some(i => i.word === word);
}
